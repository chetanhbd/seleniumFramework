public class SAMPLE3 {
    //static -  to access the static-  object is not required.
    //datatype: int, char, long, float, boolean etc.....
    //Syntax(rule): <accessmodifier> <returntype> <functionname>

    public static int addition (int a, int b){ //a and b are parameters
        // a and b have the scope inside addition function only(access modifier-private)
        return a+b;
    }

    public static double addition(double a, double b){

        return a+b;
    }


}
